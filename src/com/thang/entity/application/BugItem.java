package com.thang.entity.application;

public class BugItem {

	private long id;
	private long bug;
	private String title;
	
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public long getBug() {
		return bug;
	}
	public void setBug(long bug) {
		this.bug = bug;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	
}
